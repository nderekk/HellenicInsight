# 4. The Distillation Loop
labeled_dataset = []

for article in test_articles:
    try:
        # We don't need to put the system prompt in the string anymore!
        # The new SDK has a dedicated place for system instructions.
        
        response = client.models.generate_content(
            model='gemini-2.5-flash',
            contents=article, # Just pass the raw article here
            config=types.GenerateContentConfig(
                system_instruction=system_prompt,       # Pass your taxonomy prompt here!
                response_mime_type="application/json",  # FORCES native JSON output. No markdown!
                temperature=0.1                         # Low temperature = strict formatting
            )
        )
        
        # Because we forced JSON MIME type, there are no markdown blocks to strip.
        # We can parse it immediately.
        parsed_json = json.loads(response.text) 
        
        # Save the result
        labeled_dataset.append({
            "text": article,
            "labels": parsed_json
        })
        
        print(f"Success! Labeled: {parsed_json}")
        time.sleep(4) # Protect against free-tier rate limits
        
    except Exception as e:
        print(f"Error labeling article: {e}")
        # Pro-tip: Print the raw text if it fails so you can see WHY it failed
        if 'response' in locals():
            print(f"Raw output was: {response.text}")

